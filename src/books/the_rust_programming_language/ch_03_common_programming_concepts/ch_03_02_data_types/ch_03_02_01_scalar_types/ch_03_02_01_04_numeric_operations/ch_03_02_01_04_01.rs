pub fn fn_03_02_01_04_01() {
    println!("-------------------------------------------------- 01");
    /*
      rust 支持基本的算术运算（加，减，乘，除，取余）
    */

    println!("{}", 9 + 2); // 11
    println!("{}", 9 - 2); // 7
    println!("{}", 9 * 2); // 18
    println!("{}", 9 / 2); // 4
    println!("{}", 9 as f32 / 2.0); // 4.5
    println!("{}", 9 % 2); // 1
}
