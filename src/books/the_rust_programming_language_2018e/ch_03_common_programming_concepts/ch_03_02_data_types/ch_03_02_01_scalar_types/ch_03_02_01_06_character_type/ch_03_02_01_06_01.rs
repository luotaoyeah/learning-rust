pub fn fn_03_02_01_06_01() {
    println!("-------------------------------------------------- 01");
    /*
      字符类型使用 char 标识
    */

    /*
      字符（char）的值使用单引号包裹起来，
      区别于字符串（string）的双引号
    */
    let c01: char = 'A';
    println!("{}", c01);

    let c02: char = '😄';
    println!("{}", c02);
}
