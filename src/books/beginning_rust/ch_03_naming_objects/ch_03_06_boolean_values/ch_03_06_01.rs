pub fn fn_03_06_01() {
    println!("-------------------------------------------------- 01");
    // boolean 类型的值可以为 true 或者 false

    let b01: bool = true;
    let b02: bool = false;
    let b03: bool = 3 > 2;

    println!("{} {} {} {}", b01, b02, b03, 5 <= 5); // true false true true
}
